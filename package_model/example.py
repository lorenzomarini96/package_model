"Module to compute the square of a number"

def quadrato(a):
    """
    Parameters
    ----------
    a : float
        The number.
    Returns
    -------
    None
        Return the square of the number.
    Examples
    --------
    >>> n = 3
    >>> s = quadrato(n)
    >>> print(f's = {s}')
    >>> s = 9
    """
    return a**2
    